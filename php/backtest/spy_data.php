class ptypes
{

    const DATE           = 0;
    const OPEN           = 1;
    const HIGH           = 2;
    const LOW            = 3;
    const CLOSE          = 4;
    const VOLUME         = 5;
    const ADJUSTED_CLOSE = 6;
    const _CLOSE         = 6;   // default closing price to use

    public static function name($var)
    {
        switch ($var) {
            case ptypes::DATE:
                return 'date';
            case ptypes::OPEN:
                return 'open';
            case ptypes::HIGH:
                return 'high';
            case ptypes::LOW:
                return 'low';
            case ptypes::CLOSE:
                return 'close';
            case ptypes::VOLUME:
                return 'volume';
            case ptypes::ADJUSTED_CLOSE:
                return 'adjClose';
        }
    }

}

